package creational.prototype;

public class Coordinates {
	private Integer x = 0;
	private Integer y = 0;

	public Coordinates() {
	}

	public Coordinates(Integer x, Integer y) {
		this.x = x;
		this.y = y;
	}

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}
}
